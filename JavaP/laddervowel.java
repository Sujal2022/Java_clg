class laddervowel 
{
    public static void main(String args[])
    {
        char ch='n';

        if(ch=='a')
        System.out.println("Vowel");
        else if(ch=='e')
        System.out.println("Vowel");
        else if(ch=='i')
        System.out.println("Vowel");
        else if(ch=='o')
        System.out.println("Vowel");
        else if(ch=='u')
        System.out.println("Vowel");
        else
        System.out.println("Consonant");
    }
}
